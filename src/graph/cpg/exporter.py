def _dot_safe_id(node_id: Any) -> str:
    """Convert an arbitrary node identifier to a DOT-safe string.

    DOT identifiers must start with a letter or underscore and contain
    only alphanumeric characters or underscores.  This helper prefixes
    numeric or otherwise unsafe IDs with ``n_``.

    Args:
        node_id: The raw node identifier from the graph.

    Returns:
        A string safe for use as a DOT node identifier.
    """
    s = str(node_id)
    # If the id is purely numeric or starts with a digit, prefix it.
    if s.isdigit() or (s and s[0].isdigit()):
        return f"n_{s}"
    # Replace any non-alphanumeric characters with underscores.
    return "".join(c if c.isalnum() or c == "_" else "_" for c in s)
